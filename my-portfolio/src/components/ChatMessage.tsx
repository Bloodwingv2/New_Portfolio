import React, { useRef, useMemo } from 'react';
import { useGSAP } from '@gsap/react';
import gsap from 'gsap';
import { Bot, User, Download, ChevronLeft, ChevronRight } from 'lucide-react';
import { portfolioData } from '../data/portfolioData';
import ProjectCard from './ProjectCard';
import SkillsDisplay from './SkillsDisplay';
import ExperienceTimeline from './ExperienceTimeline';
import BioCard from './BioCard';
import ReactMarkdown from 'react-markdown';
import remarkGfm from 'remark-gfm';

interface ChatMessageProps {
    role: 'agent' | 'user';
    content: string | React.ReactNode;
    timestamp?: string;
    onProjectSelect?: (project: any) => void;
}


const ProjectDeck: React.FC<{ projects: typeof portfolioData.projects; onSelect: (p: any) => void }> = ({ projects, onSelect }) => {
    const [currentIndex, setCurrentIndex] = React.useState(0);
    const [itemsPerPage, setItemsPerPage] = React.useState(2);

    React.useEffect(() => {
        const handleResize = () => {
            setItemsPerPage(window.innerWidth < 768 ? 1 : 2);
        };
        handleResize(); // Initial check
        window.addEventListener('resize', handleResize);
        return () => window.removeEventListener('resize', handleResize);
    }, []);

    const maxIndex = Math.max(0, projects.length - itemsPerPage);

    const nextSlide = () => {
        if (currentIndex < maxIndex) {
            setCurrentIndex(prev => prev + 1);
        }
    };

    const prevSlide = () => {
        if (currentIndex > 0) {
            setCurrentIndex(prev => prev - 1);
        }
    };

    return (
        <div className="relative w-full max-w-[640px] mx-auto my-6">

            {/* Main Stage - Shows 2 cards (desktop) or 1 (mobile) */}
            <div className="overflow-hidden rounded-xl border border-gray-800 bg-gray-900/50 backdrop-blur-sm relative h-80 sm:h-96">
                <div
                    className="flex transition-transform duration-500 ease-in-out h-full"
                    style={{ transform: `translateX(-${currentIndex * (100 / itemsPerPage)}%)` }}
                >
                    {projects.map((p) => (
                        <div
                            key={p.id}
                            className={`shrink-0 p-3 flex items-center justify-center h-full ${itemsPerPage === 1 ? 'w-full' : 'w-1/2'}`}
                        >
                            <div className="w-full h-full transform transition-all duration-300 hover:scale-105">
                                <ProjectCard project={p} onClick={() => onSelect(p)} />
                            </div>
                        </div>
                    ))}
                </div>
            </div>

            {/* Navigation Controls */}
            <button
                onClick={prevSlide}
                disabled={currentIndex === 0}
                className="absolute -left-3 sm:-left-12 top-1/2 -translate-y-1/2 p-2 sm:p-3 text-white disabled:opacity-20 hover:text-blue-400 transition-colors z-20 bg-black/50 rounded-full"
            >
                <ChevronLeft size={24} className="sm:w-7 sm:h-7" />
            </button>

            <button
                onClick={nextSlide}
                disabled={currentIndex >= maxIndex}
                className="absolute -right-3 sm:-right-12 top-1/2 -translate-y-1/2 p-2 sm:p-3 text-white disabled:opacity-20 hover:text-blue-400 transition-colors z-20 bg-black/50 rounded-full"
            >
                <ChevronRight size={24} className="sm:w-7 sm:h-7" />
            </button>

            {/* Pagination Dots - One dot per slide position */}
            <div className="flex justify-center gap-2 mt-4">
                {Array.from({ length: maxIndex + 1 }).map((_, idx) => (
                    <button
                        key={idx}
                        onClick={() => setCurrentIndex(idx)}
                        className={`w-2 h-2 rounded-full transition-all duration-300 ${idx === currentIndex ? 'bg-blue-500 w-6' : 'bg-gray-700 hover:bg-gray-500'
                            }`}
                    />
                ))}
            </div>
        </div>
    );
};

const RichMessageContent: React.FC<{ content: string; onProjectSelect?: (project: any) => void }> = ({ content, onProjectSelect }) => {
    // Memoize the expensive parsing logic
    const parts = useMemo(() => {
        if (!content) return [];
        return content.split(/({{PROJECTS}}|{{SKILLS}}|{{RESUME}}|{{EXPERIENCE}}|{{BIO}})/g);
    }, [content]);

    return (
        <div className="whitespace-pre-wrap">
            {parts.map((part, index) => {
                if (part === '{{PROJECTS}}') {
                    return (
                        <div key={index} className="mt-6 mb-2">
                            <ProjectDeck projects={portfolioData.projects} onSelect={(p) => onProjectSelect?.(p)} />
                        </div>
                    );
                }
                if (part === '{{BIO}}') {
                    return (
                        <div key={index} className="mt-6 mb-2">
                            <BioCard />
                        </div>
                    );
                }
                if (part === '{{EXPERIENCE}}') {
                    return (
                        <div key={index} className="mt-6 mb-2">
                            <ExperienceTimeline experiences={portfolioData.experience} />
                        </div>
                    );
                }
                if (part === '{{SKILLS}}') {
                    return (
                        <div key={index} className="mt-4 not-prose">
                            <SkillsDisplay skills={portfolioData.skills} />
                        </div>
                    );
                }
                if (part === '{{RESUME}}') {
                    return (
                        <div key={index} className="mt-4 not-prose">
                            <a
                                href={portfolioData.resumeUrl}
                                target="_blank"
                                rel="noopener noreferrer"
                                className="inline-flex items-center gap-2 px-6 py-3 bg-white text-black font-bold rounded-lg hover:bg-gray-200 transition-colors"
                            >
                                <Download size={20} />
                                Download Resume
                            </a>
                        </div>
                    );
                }
                if (!part.trim()) return null;

                return (
                    <div key={index} className="inline-block w-full">
                        <ReactMarkdown
                            remarkPlugins={[remarkGfm]}
                            components={{
                                strong: ({ node, ...props }: any) => <span className="font-bold text-white" {...props} />,
                                ul: ({ node, ...props }: any) => <ul className="list-disc list-outside ml-4 my-2 space-y-1 text-gray-300" {...props} />,
                                ol: ({ node, ...props }: any) => <ol className="list-decimal list-outside ml-4 my-2 space-y-1 text-gray-300" {...props} />,
                                li: ({ node, ...props }: any) => <li className="pl-1 leading-relaxed" {...props} />,
                                p: ({ node, ...props }: any) => <p className="mb-2 last:mb-0 leading-relaxed text-gray-200" {...props} />,
                                a: ({ node, ...props }: any) => <a className="text-blue-400 hover:text-blue-300 hover:underline transition-colors font-medium" target="_blank" rel="noopener noreferrer" {...props} />,
                                code: ({ node, ...props }: any) => <span className="font-mono text-sm text-yellow-200/90 bg-white/5 px-1 py-0.5 rounded" {...props} />,
                                h1: ({ node, ...props }: any) => <h1 className="text-xl font-bold text-white mb-2 mt-4" {...props} />,
                                h2: ({ node, ...props }: any) => <h2 className="text-lg font-bold text-white mb-2 mt-4" {...props} />,
                                h3: ({ node, ...props }: any) => <h3 className="text-base font-bold text-white mb-1 mt-3" {...props} />,
                                // Simplified blockquote
                                blockquote: ({ node, ...props }: any) => <blockquote className="border-l-4 border-gray-700 pl-4 py-1 my-2 italic text-gray-400" {...props} />,
                            }}
                        >
                            {part}
                        </ReactMarkdown>
                    </div>
                );
            })}
        </div>
    );
};

// Use React.memo for the entire message component to prevent re-renders of old messages during streaming
const ChatMessage: React.FC<ChatMessageProps> = React.memo(({ role, content, onProjectSelect }) => {
    const containerRef = useRef<HTMLDivElement>(null);

    useGSAP(() => {
        gsap.from(containerRef.current, {
            y: 20,
            opacity: 0,
            duration: 0.5,
            ease: "power3.out"
        });
    }, { scope: containerRef });

    return (
        <div ref={containerRef} className={`flex w-full mb-6 ${role === 'user' ? 'justify-end' : 'justify-start'}`}>
            <div className={`flex max-w-[85%] md:max-w-[70%] gap-3 ${role === 'user' ? 'flex-row-reverse' : 'flex-row'}`}>

                {/* Avatar */}
                <div className={`w-8 h-8 rounded-full flex items-center justify-center shrink-0 ${role === 'agent' ? 'bg-white text-black' : 'bg-gray-800 text-gray-300'
                    }`}>
                    {role === 'agent' ? <Bot size={18} /> : <User size={18} />}
                </div>

                {/* Bubble */}
                <div className={`flex flex-col gap-1 ${role === 'user' ? 'items-end' : 'items-start'}`}>
                    <div className={`px-4 py-3 rounded-2xl text-sm md:text-base leading-relaxed ${role === 'agent'
                        ? 'bg-gray-900 border border-gray-800 text-gray-200 rounded-tl-sm'
                        : 'bg-white text-black rounded-tr-sm'
                        }`}>
                        {/* If content is string and agent, use Rich renderer. Else just render node/string */}
                        {role === 'agent' && typeof content === 'string' ? (
                            <RichMessageContent content={content} onProjectSelect={onProjectSelect} />
                        ) : (
                            content
                        )}
                    </div>
                </div>

            </div>
        </div>
    );
});

export default ChatMessage;
